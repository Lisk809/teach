# 总结
# 一、容器类型
# 列表、元组、字典、集合

# 二、标量、原子类型
# 整型、浮点型、字符串


# 可变不可变
# 一、可变类型
# 列表、字典、集合

# 二、不可变类型
# 整型、浮点型、字符串、元组


# 访问方式区分
# 1、通过变量名访问
# 整型、浮点型
# 2、通过索引访问指定的值（序列类型）
# 字符串、列表、元组
# 3、通过key访问指定的值（映射类型）
# 字典
# 4、通过变量名访问整体（无法访问某一个值）
# 集合

